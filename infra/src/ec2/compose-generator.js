/**
 * Generates docker-compose.yml content for a single database service on the EC2 host.
 */

import { engines } from './engines.js';

/**
 * Resolve mem_limit/cpus for a given engine. Precedence (highest first):
 *   1. Explicit `resources` in config
 *   2. Env vars DB_HOST_<ENGINE>_MEM_LIMIT / DB_HOST_<ENGINE>_CPUS
 *   3. engine.default_resources
 * Lets ops tune per-engine ceilings on a host without redeploying code.
 */
function resolve_resources(engine, eng, override, env = process.env) {
    const up = engine.toUpperCase();
    const def = eng.default_resources || {};
    return {
        mem_limit: override?.mem_limit ?? env[`DB_HOST_${up}_MEM_LIMIT`] ?? def.mem_limit,
        cpus: override?.cpus ?? env[`DB_HOST_${up}_CPUS`] ?? def.cpus,
    };
}

/**
 * @param {{ name: string, engine: string, version?: string, port: number, db_name: string, data_dir: string, seeds_dir?: string, resources?: { mem_limit?: string, cpus?: string } }} config
 * @returns {string} docker-compose.yml content
 */
export function generate_compose(config) {
    const { name, engine, version, port, db_name, data_dir, seeds_dir, resources } = config;

    const eng = engines[engine];
    if (!eng) throw new Error(`Unknown engine: ${engine}`);

    const image = eng.image_template.replace('{version}', version || eng.default_version);
    const env = eng.env_vars({ db_name });
    const hc = eng.healthcheck;
    const host_data = `${data_dir}/${name}`;
    const { mem_limit, cpus } = resolve_resources(engine, eng, resources);

    const env_lines = Object.entries(env)
        .map(([k, v]) => `      ${k}: "${v}"`)
        .join('\n');

    const test_value = Array.isArray(hc.test)
        ? JSON.stringify(hc.test)
        : `"${hc.test}"`;

    let volumes = `      - ${host_data}:${eng.container_data_path}`;
    if (seeds_dir) {
        volumes += `\n      - ${seeds_dir}:/docker-entrypoint-initdb.d:ro`;
    }

    const limit_lines = [];
    if (mem_limit) limit_lines.push(`    mem_limit: ${mem_limit}`);
    if (cpus) limit_lines.push(`    cpus: ${cpus}`);
    const limits_block = limit_lines.length ? `${limit_lines.join('\n')}\n` : '';

    return `# Auto-generated by infra-compose for db-host
# Service: ${name} (${engine})

services:
  ${name}:
    image: ${image}
    container_name: ${name}
    restart: unless-stopped
${limits_block}    ports:
      - "${port}:${eng.default_port}"
    environment:
${env_lines}
    volumes:
${volumes}
    healthcheck:
      test: ${test_value}
      interval: ${hc.interval}
      timeout: ${hc.timeout}
      retries: ${hc.retries}
`;
}
